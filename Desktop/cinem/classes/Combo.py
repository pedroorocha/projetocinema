class Combo():
    def __init__(self, CodComida, comida, valor, IdCliente):
        self.CodComida = CodComida
        self.comida = comida
        self.valor = valor
        self.IdCliente = IdCliente

def getCodComida(self):
    return self.__CodComida
def setCodComida(self, CodComida):
    self.__CodComida = CodComida
def getComida(self):
    return self.__comida
def setComida(self, comida):
    self.__comida = comida
def getValor(self):
    return self.__valor
def getIdCliente(self):
    return self.__IdCliente
def setIdCliente(self, IdCliente):
    self.__IdCliente = IdCliente