    public int buscaId(String nome, String senha){
        for(int i = 0; i<cliente.ler().size();i++){
            if(cliente.ler().get(i).getNome().equals(nome) && cliente.ler().get(i).getSenha().equals(senha)){
                this.setId(cliente.ler().get(i).getId());
            }
        }
        return this.getId();
    }

