    public void salvaCompra(){
         Connection con = ConnectionFactory.getConnection();
        PreparedStatement stmt  = null;
        
        try {
            stmt = con.prepareStatement("INSERT INTO compras(codigo,filme,sess√£o,total) VALUES(?,?,?,?)");
            //stmt.setInt(1,);
            stmt.setString(2, filme.getText());
            stmt.setString(3, sessao.getText());
            stmt.setString(4,valor.getText());
            stmt.executeUpdate();
            JOptionPane.showMessageDialog(null, "cadastro realizado com sucesso!");
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "falha no casdatro");
            Logger.getLogger(ClienteDao.class.getName()).log(Level.SEVERE, null, ex);
        }finally{
            ConnectionFactory.closeConnection(con, stmt);
        }
    }   
